# Python Variable Datatypes

## int
# Stores whole numbers (positive or negative).
x = 5         # Positive integer
y = -10       # Negative integer

## float
# Stores decimal (floating-point) numbers.
a = 3.14      # Positive float
b = -0.001    # Negative float

## str
# Stores sequences of characters (text).
name = "Hello, World!"  # String
num_as_str = "123"      # String that looks like a number

## bool
# Stores boolean values: True or False.
is_valid = True         # Boolean true
is_done = False         # Boolean false

## list
# Ordered collection of items (mutable).
my_list = [1, 2, 3, 4]            # List of integers
mixed_list = [1, "Hello", 3.5]    # List with mixed types

## tuple
# Ordered collection of items (immutable).
my_tuple = (1, 2, 3)              # Tuple of integers
mixed_tuple = (1, "Hello", 3.5)   # Tuple with mixed types

## dict
# Key-value pairs where keys are unique.
person = {"name": "Alice", "age": 25}  # Dictionary with string keys

## set
# Unordered collection of unique elements.
my_set = {1, 2, 3, 3, 2, 1}       # Set with duplicates automatically removed