# Python Variable Datatypes

## int (heltal)
# Description: Stores integer values (whole numbers, positive or negative).
# Example:
x = 5      # Example of an integer variable
y = -10    # Example of a negative integer

## float (flyttal)
# Description: Stores decimal (floating point) numbers.
# Example:
a = 3.14   # Example of a float variable
b = -0.001 # Example of a negative float

## str (sträng)
# Description: Stores sequences of characters (text).
# Example:
name = "Hello, World!" # Example of a string variable
num_as_str = "123"     # String that looks like a number

## bool (boolean)
# Description: Stores boolean values, either True or False.
# Example:
is_valid = True  # Boolean True value
is_done = False  # Boolean False value

## list (lista)
# Description: Stores an ordered collection of items, which can be of any data type.
# Example:
my_list = [1, 2, 3, 4]        # List of integers
mixed_list = [1, "Hello", 3.5] # List of mixed types

## tuple (tupler)
# Description: Stores an ordered collection of items, similar to lists, but immutable (unchangeable).
# Example:
my_tuple = (1, 2, 3)          # Tuple of integers
mixed_tuple = (1, "Hello", 3.5) # Tuple of mixed types

## dict (ordbok)
# Description: Stores key-value pairs, where each key is unique.
# Example:
person = {"name": "Alice", "age": 25} # Dictionary with string keys

## set (mängd)
# Description: Stores an unordered collection of unique elements.
# Example:
my_set = {1, 2, 3, 3, 2, 1}  # Set with unique integers (duplicates are removed automatically)
